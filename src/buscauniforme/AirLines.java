    private void preenche(int i) {
        DefaultComboBoxModel model = new DefaultComboBoxModel();
        // abertura do arquivo
        try {
            BufferedReader myBuffer = new BufferedReader(new InputStreamReader(new FileInputStream("src\\Arquivos\\cidades.txt"), "ISO-8859-1"));

            // loop que lÃª e imprime todas as linhas do arquivo
            String cidade = myBuffer.readLine();

            while (cidade != null) {
                model.addElement(cidade);
                cidade = myBuffer.readLine();
            }

            myBuffer.close();
            switch (i) {
                case 1:
                    orig.setModel(model);
                    break;
                case 2:
                    dest.setModel(model);
                    break;
                default:
                    break;
            }
        } catch (Exception e) {

        }

    }

    private void popularGrafo() {
        try {
            File f = new File("src\\Arquivos\\rotas.txt");
            BufferedReader brc = new BufferedReader(new InputStreamReader(new FileInputStream("src\\Arquivos\\cidades.txt"), "ISO-8859-1"));
            String cid = brc.readLine();

            while (cid != null) {
                Grafo.addVertex(cid);
                cid = brc.readLine();
            }
            brc.close();

            FileReader fr = new FileReader(f);
            BufferedReader br = new BufferedReader(fr);
            String linha;
            while ((linha = br.readLine()) != null) {
                String rota[] = new String[3];
                rota = linha.split(";");
                int start = Integer.parseInt(rota[0]);
                int end = Integer.parseInt(rota[1]);
                double value = Double.parseDouble(rota[2]);
               
                Grafo.addEdge(start, end, value);
                

            }
            
            br.close();
            

        } catch (Exception e) {

        }
        
    }
    
